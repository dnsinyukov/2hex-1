<font style="font-size: 130%"><b>COMPLETE SKATEBOARDS ARE MORE IMPORTANT TO YOUR COMPANY THAN YOU MIGHT THINK.</b></font><br>
                        <br>                                
unlike traditional businesses, skateboard companies strongly focus their attention on their perceived style and image. <br>
These brands look for products that best improve their brand image. <br>
<br>
They decide against traditional market analyses or checking product margins and instead only produce custom skateboard decks and grip tapes.These however are the two skateboard products with the lowest margins.  On top of that, both of these products are highly price competitive.<br>
<br>
Building up a great image is a good base for any company. But still it’s only a base and should not define ones whole company.<br> 
<br>
Once you have sold shops your first decks and grip tapes, follow up with great complete skateboards or component-sets therefor!<br>
Ask any shop what skateboard products are sold most frequently, and they will tell you its skateboard completes or low cost components used to build completes. The same shop will most likely also confirm to you, that these are the two products that their distributor have the smallest offer to choose from.<br>
<br>
<br>
<font style="font-size: 130%"><b>MAKING SKATEBOARD COMPLETES DOES NOT MEAN THAT YOU CAN STEP BACK FROM MAKING GREAT DESIGNS.</b></font><br>
<br>
When visiting a skateshop, you will see that most pro decks come with great designs, but most complete skateboards are no pieces of art! <br>
Most skateboard brands that offer complete skateboards, plan these with very little focus to detail as they see it as only a side product. <br>
<br>
However, the group of skateboard-beginners is much bigger and often has more money than experienced skateboarders. <br>
<br>
Therefor: Create complete skateboards with a strong focus to detail. Let your designer turn all 6 skateboard components into one coherent piece of art! <br>
You can be sure, every skate shop you present your complete skateboards to will become a customer!<br>
