                                                        <br>
                                                        <p>
                                                            <b>Most skateboard companies fail.</b><br>
                                                            Other skateboard companies only survive because their founders work 2 jobs to cover the production costs.<br>
                                                            <br>
                                                            There is <b>one reason</b> why almost all skateboard companies fail!<br>
                                                            And there are <b>5 steps</b> how you can make real money from your skateboard business!<br>
                                                            <br>
                                                            For over 10 years 2HEX runs skateboard productions for skateboard companies from all over the world. Some companies have become very successful, many other have closed.<br>
                                                            <br>
                                                            So we went out to find characteristics all successful companies have in common, but none of the failing companies shared.<br>
                                                            <br>
                                                            The result is clear. There is one major reason why almost all skateboard companies fail and there are 5 steps all successful companies have taken to become well known and profitable.<br>
                                                            <br>
                                                            Follow these 5 steps and your skateboard business will succeed. To which extend you follow
                                                            these 5 steps determines how fast your skateboard company will grow.<br>
                                                            <br>
                                                            <br>
                                                            We here at 2HEX don’t want new founders to go through the same mistakes of previous  skateboard companies again and again.<br>
                                                            <br>
                                                            <br>
                                                            <br>
                                                            <font style="font-size: 130%"><b>The major reason skateboard companies fail:</b></font><br>
                                                            <br>
                                                            Most skateboard companies fail because they put too little time and focus into sales.
                                                            Most founders continuously improve their designs, sponsor more skateboarders, arrange more
                                                            events and wait for shops to come and ask for their skateboard components. Far not enough shops
                                                            come and ask for boards, so founders get a job to keep their skateboard business alive. With a safe
                                                            income and very limited time, these founders then focus even less on sales. This is the path to certain death.<br>
                                                            <br>
                                                            Don’t go down the same road. Learn how successful founders tackle sales!<br>
                                                            <br>
                                                            <br>
                                                            <font style="font-size: 130%"><b>The following 5 sales tactics were followed by all of our successful customers.</b></font><br>
                                                            <br>
                                                            <br>
                                                            <font style="font-size: 130%"><b>1. Sales are first priority</b></font><br>
                                                            <br>
                                                            No matter your vision or company’s goal… your company needs to make money. Without money, you can’t run productions, you can’t invest all your time and you can’t support skateboarding. <br>
                                                            There is only one way you can make money. You have to sell. Selling should always be your number one priority. Not sponsoring, not design, not marketing, not your taxes and bills. Sales. <br>
                                                            Start by making a list of all skate shops and skateboard distributions within 300km of your location. Call them, visit them and start selling. If you have stock, pack a big bag and visit the shop. Try to leave with an empty bag. If you don’t yet have skateboards, offer them to sell your skateboard lessons to their customers to grow their market. <br>

                                                            <br>
                                                            <br>
                                                            <font style="font-size: 130%"><b>2. Make your customers’ skate shops grow</b></font><br>
                                                            <br>
                                                            Think about what you can offer your customer to make his business grow! It’s no help to have your skateboards hanging in a store that has no sales. There are two ways to grow a skate shop’s business: Bring more skateboarders to buy at the shop AND turn more people into skateboarders. Usually its kids and their parents that bring most money into skate shops. To get more kids to skate,
                                                            help skate shops offer skateboarding lessons, arrange demos and events together with the shop in schools or other places that have many kids. By doing so you don’t just hook the shop but also the shop’s next generation of clients to your brand!<br>

                                                            <br>
                                                            <br>
                                                            <font style="font-size: 130%"><b>3. Work according to a calendar</b></font><br>
                                                            <br>
                                                            Follow up closely with your clients. Call each customer every two weeks to check if they have enough stock of all of your products. If a shop does not sell your products, ask why they aren’t selling and how you can help the shop sell your goods. Don’t offer lower prices, offer more support as described above.<br>
                                                            Be well prepared and schedule your annual tasks. Have your warehouse filled before the first days of spring and have enough stock to make it all the way until your next production arrives.<br>
                                                            <br>
                                                            <br>
                                                            <font style="font-size: 130%"><b>4. Manage your finances</b></font><br>
                                                            <br>
                                                            Don’t overspend on sponsoring. Firstly make sure that you have enough money to grow every production run. Secondly make sure you pay yourself high enough of a salary, that you can invest all your time into your skateboard business. Thirdly make sure that you have enough money to visit and support your customers. The rest of the money should go into design and marketing. Sponsoring is only one component of marketing.<br>
                                                            <br>
                                                            Don’t mix private and company money. Use separate bank accounts and wallets. Pay yourself once per month a salary from the business account.<br>
                                                            <br>
                                                            Know your product margins! Don’t dismiss direct sales, if you sell directly to an end user, your margin is three times as high.<br>
                                                            Decks are great for your company’s image, but completes make the volume and money! There are much more beginners than there are pros, but the offer for beginners is very limited. Sell decks to keep a great brand image. Sell completes to make money. <br>
                                                            <br>
                                                            <br>
                                                            <font style="font-size: 130%"><b>5. Let a successful skateboard business guide you</b></font><br>
                                                            <br>
